def solve():
# Ниже пишите решение задачи
    # Задача 1: Плоскоземельщики
    x = int(input())
    
    # Площадь сферы = 4 * π * r²
    # Площадь круга = π * R²
    # Приравниваем: 4 * π * x² = π * R²
    # Отсюда: R² = 4 * x², значит R = 2 * x
    
    radius_tarelka = 2 * x
    
    # Диаметр тарелки = 2 * R = 4 * x
    # Время перелета = расстояние / скорость = (4 * x) / 900
    perelet_time = (4 * x) // 900
    
    print(radius_tarelka, perelet_time)

# Код ниже не трогать! он нужен для тестов
if __name__ == "__main__":
    solve()
